# (CBJ) Basic Math Operators
a, b, c = 1, 3, 7

print("a is:", a)
print("b is:", b)
print("c is:", c)

print("summation: a+b+c", a+b+c)          # (CBJ) '+'  add operator
print("subtraction: a-b-c", a-b-c)        # (CBJ) '-'  minus operator
print("multiplication: a*b*c", a*b*c)     # (CBJ) '*'  multiplication operator
print("power: c**b", c**b)                # (CBJ) '**' exponential operator
print("divide: c/b", c/b)                 # (CBJ) '/'  divide operator
print("floor division: c//b", c//b)       # (CBJ) '//' floor division operator
print("modular: c%b", c % b)              # (CBJ) '%"  modular operator